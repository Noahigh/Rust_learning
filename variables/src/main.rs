fn main() {
    println!("Hello, world!");

    let x = 5;

    println!("The value of x is: {}", x);

    // x = 6;  // 这是错误的
    let x = 6;
    println!("The value of x is: {}", x);

    // ==========================
    let mut y = 2;  // 声明可变变量

    println!("The value of y is: {}", y);

    y = 3;

    println!("The value of y is: {}", y);

}
